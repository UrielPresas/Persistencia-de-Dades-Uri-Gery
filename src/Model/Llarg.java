    private int via_id;
    private int llargada_llarg;
    private int NºLlarg;
    private int llargada_total;

    public long getId_llarg() {
        return id_llarg;
    }

    public int getVia_id() {
        return via_id;
    }

    public int getLlargada_llarg() {
        return llargada_llarg;
    }

    public int getNºLlarg() {
        return NºLlarg;
    }

    public int getLlargada_total() {
        return llargada_total;
    }

    public void setId_llarg(long id_llarg) {
        this.id_llarg = id_llarg;
    }

    public void setVia_id(int via_id) {
        this.via_id = via_id;
    }

